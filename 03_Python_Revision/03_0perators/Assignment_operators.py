# Assignment operators 
# =, +=, -=, *=, /=, %=, **=, //=, <<=,
# >>=, >>>=, &=, ^=, |=

# example 
x = 5
x = x + 5
print(x)  # prints 10
x += 5
print(x)  # prints 15
x -= 5
print(x)  # prints 10
x *= 5
print(x)  # prints 50
x /= 5
print(x)  # prints 10.0
x %= 5
print(x)  # prints 0
x **= 5
print(x)  # prints 0
x //= 5
print(x)  # prints 0
x <<= 5
print(x)  # prints 0
x >>= 5
print(x)  # prints 0
