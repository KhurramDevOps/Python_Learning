# end parameter 
# end parameter let u control what appear at the end of the print statement
#  by default print statement end after the statement
print("welcome to ", end = " ")
print("the party")


# sep parameter 
# sep means seperator 
# specifies the seperator between multiple objects
print ("khurram" ,"is" ,"student " , sep = "_")

print("helle","bye","hy", sep = "\n")
# sep parameter let u control what appear between the print statement
# by default print statement use space as a seperator