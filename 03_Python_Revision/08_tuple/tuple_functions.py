# TUPLE FUNCTIONS 

# 1.  count() :  This function returns the number of elements with the specified value.
# EXAMPLE :

tuple1 = (1, 2, 3, 4, 5)
print(tuple1.count(4)) 

# 2.  index() :  This function returns the index of the first occurrence of the specified value.
# EXAMPLE :

tuple1 = (1, 2, 3, 4, 5)
print(tuple1.index(4))

