# INTEGER DATA TYPE 
# EVERY NUMBER IS CALL A INTEGER 
# EXAMPLE
number :int = 78
print(type(number))
even_numbers = 2,4,6,8,10
print(even_numbers)


# FLOAT DATA TYPE 
# FLOAT IS CALLED WHEN A NUMBER CONTAIN DECIMAL POINT IN IT 
# EXAMPLE 

num : float = 12.5
print(type(num))

# COMPLEX NUMBERS

#  COMPLEX NUMBER IS A NUMBER CONSIST OF REAL AND IMAGINARY PART 
# EXAMPLE 

value : complex = 3 + 2j
print(type(value))
